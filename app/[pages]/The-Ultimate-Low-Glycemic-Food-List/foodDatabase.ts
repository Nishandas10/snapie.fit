// NOTE: The list above is curated manually and may occasionally gain duplicates
// during expansions. To guarantee the UI never shows duplicates, we export a
// de-duplicated view (first occurrence wins).
const _dedupedFoodDatabaseMap = new Map<string, FoodItem>();
for (const item of foodDatabase) {
  const key = item.name.trim().toLowerCase();
  if (!_dedupedFoodDatabaseMap.has(key)) {
    _dedupedFoodDatabaseMap.set(key, item);
  }
}

const _duplicateCount = foodDatabase.length - _dedupedFoodDatabaseMap.size;
if (process.env.NODE_ENV !== "production" && _duplicateCount > 0) {
  // Keep this as a warning only (no runtime crash)
  console.warn(
    `[foodDatabase] Removed ${_duplicateCount} duplicate item(s) by name during export.`,
  );
}

// Export a clean, non-duplicated view
export const foodDatabaseUnique: FoodItem[] = Array.from(
  _dedupedFoodDatabaseMap.values(),
);

// Get all unique categories
export const categories = [
  "All",
  ...Array.from(new Set(foodDatabase.map((item) => item.category))),
];

// Get total count
export const totalFoodCount = foodDatabaseUnique.length;
